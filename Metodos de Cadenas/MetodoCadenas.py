#Metodos para manipular cadenas
#String

nombreUsuario=input("Introduce un nombre de usuario: ")
print("El nombres es: ", nombreUsuario)

#Lo Pasamos a Mayusculas
print("El nombres es: ", nombreUsuario.upper())

#Lo Pasamos a Minusculas
print("El nombres es: ", nombreUsuario.lower())

#Lo Pasamos a Letracapital
print("El nombres es: ", nombreUsuario.capitalize())
