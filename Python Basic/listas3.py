nombres = ["Brian", "Edith", "Gines", "Roxana", "Mafer", "Rosario"]

# Preguntar el largo de una lista
print(len(nombres))

# agregar un nuevo elemento a la lista
nombres.append("MARIA")
print(nombres)

# Insertar un elemento Indice
nombres.insert(1, "mama")
print(nombres)

# Remover un elemento de la lista
nombres.remove("mama")
print(nombres)